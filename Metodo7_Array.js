//Exemplo 1
//criei a variável posiçãoCorrida e atribuí a ela um valor
let posiçãoCorrida = ['primeiro', 'segundo', 'terceiro'];

//usei o console.log para exibir o valor da posiçãoCorrida
console.log('posição da corrida :', posiçãoCorrida);

//criei uma variável posiçãoContrária e atribuí a ela o valor inverso a posiçãoCorrida
let posiçãoContraria = posiçãoCorrida.reverse();

//exibi o valor da posiçãoContraria
console.log('posição inversa da corrida:', posiçãoContraria);

